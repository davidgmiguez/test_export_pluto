##
Another simple example is the generation of a given reactant in the system, such as:
```math
0  \overset{k_1}{\longrightarrow} X_1 \tag{8}
```

with initial concentration $X_1(0)$ and stoichometric matrices

```math
A=\begin{bmatrix}
 0\end{bmatrix} ;
B=\begin{bmatrix}
1   
\end{bmatrix} ; (B-A)^T= \begin{bmatrix}
 1 \end{bmatrix} \tag{9}
```
##
in this particular case

```math
K=\begin{pmatrix}
 k_1\\ 
\end{pmatrix} \tag{10}
```
##
and 

```math
X^A=\begin{pmatrix}
X_1^0
\end{pmatrix}=\begin{pmatrix}
1
\end{pmatrix} \tag{11}
```
##
so 

```math
\begin{align}
 \begin{bmatrix}
\frac{\mathrm{d} X_1}{\mathrm{d} t} \end{bmatrix}= \begin{bmatrix}
 1 \end{bmatrix} \begin{bmatrix}
