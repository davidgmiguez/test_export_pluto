md" ## 1. Mass Action Law for Open Systems

Despite being developed in the context of closed chemical systems, the __Mass Action Law__ is also applicable to open systems, i.e., systems where there is no conservation of mass. This is a special case in chemistry (open chemical reactors), but extremely relevant for biological systems, since biological species are often part of open systems that exchange mass, energy, or information with the environment. Let's explore how Mass action takes into account these type of open systems. 
##
The first and most simple example of an open system is when a given reactant is degraded or flows away. This is illustrated by the following interaction scheme:

```math   
X_1  \overset{k_1}{\longrightarrow} 0 \tag{1} 
``` 

with initial concentration $X_1(0)$ and stoichometric matrices

```math
A=\begin{bmatrix}
 1\end{bmatrix} ;
B=\begin{bmatrix}
0   
\end{bmatrix} ; (B-A)^T= \begin{bmatrix}
 - 1 \end{bmatrix} \tag{2}
```
 ##
in this particular case

```math
K=\begin{pmatrix}
 k_1\\ 
\end{pmatrix} \tag{3}
```
##
and 

```math
X^A=\begin{pmatrix}
X_1^1
\end{pmatrix} \tag{4}
```
##
so 

```math
 \begin{bmatrix}
\frac{\mathrm{d} X_1}{\mathrm{d} t} \end{bmatrix}= \begin{bmatrix}
 - 1 \end{bmatrix} \begin{bmatrix}
k_1 \end{bmatrix}\begin{pmatrix}
X_1
\end{pmatrix} = \begin{bmatrix}
 - 1 \end{bmatrix} \begin{pmatrix}
k_1  X_1
\end{pmatrix} = \begin{bmatrix}
 - k_1 X_1 \end{bmatrix}  
```
##
So the ODE is simply.

```math
\frac{\mathrm{d} X_1}{\mathrm{d} t} =  - k_1 X_1 \tag{6}
```
##
with a solution that is equivalent to the irreversible first order reaction.
 
```math
X_1 (t)= X_1 (0) e^{- k_1 t}  \tag{7}
```
##
"

