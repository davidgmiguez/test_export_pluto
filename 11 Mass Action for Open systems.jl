Therefore the full solutions for $X_1$ is:

```math
\begin{align}
X_1 (t) = \frac{k_2}{k_1} + \frac{(X_1 (0) \cdot k_1) - k_2 }{k_1} e^{-k_1 \cdot t} \tag{12}\\
X_1 (t) =  \frac{k_2}{k_1} + X_1 (0) \cdot e^{-k_1 \cdot t} - \frac{k_2}{k_1} e^{-k_1 \cdot t}  \tag{12}\\
X_1 (t) =  \frac{k_2}{k_1} (1-e^{-k_1 \cdot t})+ X_1 (0) \cdot e^{-k_1 \cdot t}    \tag{14}\\
\end{align}
``` 

"

