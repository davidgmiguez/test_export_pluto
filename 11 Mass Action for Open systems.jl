md"
## 3. Reversible open systems

We will try what happens if we white them as reversible reactions

```math   
X_1  \overset{k_1}{\underset{k_2}{\longleftrightarrow}} 0 \tag{1} 
``` 
which is equivalent to two irreversible reactions:

```math   
X_1  \overset{k_1}{\longrightarrow} 0 \tag{1} 
``` 

```math   
0  \overset{k_2}{\longrightarrow} X_1 \tag{1} 
``` 

with initial concentration $X_1(0)$ and stoichometric matrices

```math
A=\begin{bmatrix}
 1\\0\end{bmatrix} ;
B=\begin{bmatrix}
0 \\ 1  
\end{bmatrix} ; (B-A)^T= \begin{bmatrix}
 - 1 & 1 \end{bmatrix} \tag{2}
```
 ##
in this particular case

```math
\begin{bmatrix}
k_1 & 0\\ 
 0& k_2
\end{bmatrix} \tag{3}
```
##
and 

```math
X^A=\begin{pmatrix}
X_1^1\\ 1
\end{pmatrix} \tag{4}
```
##
so 

```math
 \begin{bmatrix}
\frac{\mathrm{d} X_1}{\mathrm{d} t} \end{bmatrix}= \begin{bmatrix}
 - 1 & 1 \end{bmatrix} \begin{bmatrix}
k_1 & 0\\ 
 0& k_2
\end{bmatrix}\begin{pmatrix}
X_1^1\\ 1
\end{pmatrix} = \begin{bmatrix}
 - 1 & 1 \end{bmatrix}\begin{pmatrix}
k_1 \cdot X_1^1\\ k_2
\end{pmatrix}  
```
##
So the ODE is simply.

```math
\frac{\mathrm{d} X_1}{\mathrm{d} t} =  - k_1 X_1  + k_2 \tag{6}
```
##

which, when we rearrange terms becomes:

```math
\frac{dX_1}{dt}+k_1 \cdot X_1= k_2  \tag{7}
```

To solve this diferential equation, we need to calculate the integration factor, $e^{ \int p(x)dx }$, that allows us to put the first term of the equation as one diferentiation. In this case $p(x)=k_1 $, so $e^{ \int k_1 \cdot dt }=e^{k_1 \cdot t}$. This way, when we multiply both terms in eq. 7 by the integrating factor, we obtain:


"

# ╔═╡ 05adf9ce-901b-4bd6-a532-9a63f7f2db9f
md"
```math
\frac{dX_1}{dt} e^{k_1 \cdot t} +k_1 \cdot e^{k_1 \cdot t} X_1 =  k_2 e^{k_1 \cdot t} \tag{8}
```

The first term now is the derivative of $X_1 \cdot  e^{k_1 \cdot t}$ so we can rewrite:

```math
\frac{d(X_1 e^{k_1 \cdot t})}{dt} = k_2 e^{k_1 \cdot t} \tag{9}
```

we move the `dt` to the right side we integrate 

```math
\int d(X_1 e^{k_1 \cdot t}) = k_2 \int e^{k_1 \cdot t} dt \tag{10}
```

solving

```math
X_1 e^{k_1 \cdot t} = \frac{k_2}{k_1}  e^{k_1 \cdot t} + C \tag{11}
```

rearranging terms becomes

```math
X_1 (t) = \frac{k_2}{k_1} + C \cdot e^{-k_1 \cdot t} \tag{12}
```
"

# ╔═╡ 25ceaba4-d9cf-4040-b95e-a6e4d64b1cb3
md"##
to calculate the integration constant we use the initial value of $X_1$

```math
\begin{align}
X_1 (0) &= \frac{k_2}{k_1} + C \cdot e^{-k_1 \cdot 0} \tag{13}\\
X_1 (0) &=\frac{k_2}{k_1} + C \tag{14}\\
C&= X_1 (0) - \frac{k_2}{k_1} \tag{15}\\
C&= \frac{(X_1 (0) \cdot k_1) - k_2 }{k_1}\tag{16} \\
\end{align}
```
"

