md"## 4. Dominance of the Fittest Model

We will now test what happens when you have two exponentially growing species in the same system. This is kind of related with the exponential and logistic growth. 


```math
\begin{align}
\frac{dX_1}{dt} = k_1 X_1 \\
\frac{dX_2}{dt} = k_2 X_2
\end{align}
```
##
we can define in this system the total amount of individuals in the system as $X_T=X_1+X_2$, so:

```math 
\begin{align}
\frac{dX_T}{dt} = k_1 X_1 + k_2 X_2\\
\end{align}
```
##
Now we can define the share of each species in the total population as: 

```math 
\begin{align}
x_1(t) = \frac{X_1(t)}{X_1(t)+X_2(t)}\\
x_2(t)= \frac{X_2(t)}{X_1(t)+X_2(t)}\\
\end{align}
```
##
Now we can see what happens to the share of each species as time goes to infinity, taking into account the time evolution of the individual populations

```math 
\begin{align}
X_1(t) = X_1(0) \cdot e^{k_1 t}\\
X_2(t) = X_2(0) \cdot e^{k_2 t}\\
\end{align}
```
##
Lets substitute this solutions in $x_1(t)$, for instance:
```math 
\begin{align}
x_1(t) = \frac{X_1(0) \cdot e^{k_1 t}}{X_1(0) \cdot e^{k_1 t}+X_2(0) \cdot e^{k_2 t}}\\
\end{align}
```
##
Let's now simplify this expression:
```math 
\begin{align}
x_1(t) = \frac{X_1(0) \cdot e^{k_1 t}}{X_1(0) ( e^{k_1 t}+ \frac{X_2(0)}{X_1(0)} \cdot e^{k_2 t})}\\
x_1(t) = \frac{\cancel{X_1(0)} \cdot e^{k_1 t}}{\cancel{X_1(0)} ( e^{k_1 t}+ \frac{X_2(0)}{X_1(0)} \cdot e^{k_2 t})}\\
x_1(t) = \frac{ e^{k_1 t}}{ e^{k_1 t}+ \frac{X_2(0)}{X_1(0)} \cdot e^{k_2 t}}\\
\end{align}
```
##
we can keep going:

```math 
\begin{align}
x_1(t) = \frac{ e^{k_1 t}}{ e^{k_1 t} (1 + \frac{X_2(0)}{X_1(0)} \cdot e^{(k_2-k_1) t})}\\
x_1(t) = \frac{ \cancel{e^{k_1 t}}}{ \cancel{e^{k_1 t}} (1 + \frac{X_2(0)}{X_1(0)} \cdot e^{(k_2-k_1) t})}\\
x_1(t) = \frac{1}{1 + \frac{X_2(0)}{X_1(0)} \cdot e^{(k_2-k_1) t}}\\
\end{align}
```
##
if we assume for instance that the growth rates are a bit different, for instance, $k_1 > k_2$, then we can study what happens as time goes to infinite. This expression in the denominator  $e^{(k_2-k_1)t}$ will become zero, since the exponent is negative by definition. So, 

```math 
\begin{align}
 \lim_{t\to\infty} x_1(t)  = \frac{1}{1 + 0} = 1\\
\end{align}
```
##
The prediction is that, if the rate constants are slightly different, the system will end up with just one species at the end. In other words, one species will dominate the other and it will take over in the long term. This is what is called _Dominance of the fittest_ in ecology.


