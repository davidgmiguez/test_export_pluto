md"
##
Let's now explore some special cases of open systems that look in principle very similar to the previous one:

```math
X_1  \overset{k_1}{\longrightarrow} 2 X_1 \tag{17}
```

and this one

```math
2X_1  \overset{k_1}{\longrightarrow} 3 X_1 \tag{18}
```
##
These two reaction schemes seem very similar to the previous case of linear production, since for every reaction you end up with a new molecule of your reactant $X_1$, so we expect similar dynamics. 
#
When we write the differential equations for these two cases we have:

```math
A=\begin{bmatrix}
 1\end{bmatrix} ;
B=\begin{bmatrix}
2   
\end{bmatrix} ; (B-A)^T= \begin{bmatrix}
 1 \end{bmatrix} \tag{19}
```
##
and for the second-order production case

```math
A=\begin{bmatrix}
 2\end{bmatrix} ;
B=\begin{bmatrix}
3   
\end{bmatrix} ; (B-A)^T= \begin{bmatrix}
 1 \end{bmatrix} \tag{20}
```
##
In fact, $(B-A)^T$ has the same value in the three cases, but $X^A$ does not. For the first-order production

```math
X^A=\begin{pmatrix}
X_1^1
\end{pmatrix} \tag{21}
```
##
and for the second order

```math
X^A=\begin{pmatrix}
X_1^2
\end{pmatrix} \tag{22}
```
##
so, following the Mass action general form for the first-order production reaction:

```math
\frac{\mathrm{d} X_1}{\mathrm{d} t} =  k_1 \cdot X_1 \tag{23}
```
## 
 and for the second order production 
 
```math
\frac{\mathrm{d} X_1}{\mathrm{d} t} = k_1 \cdot X_1^2 \tag{24}
```
 
 ##
 We know the solution for the first-order production:

```math
X_1 (t)= X_1 (0) \cdot e^{k_1 \cdot t}  \tag{25}
```
 ##
with the second one is :

```math
\begin{align}
\int \frac{\mathrm{d} X_1}{X_1^2} &=  \int k_1 \cdot \mathrm{d}t \tag{26} \\
-X_1^{-1} &=  k_1 \cdot t + C \tag{27}\\
X_1(t) &=\frac{-1}{k_1 \cdot t + C}  \tag{28}
\end{align}
```

##
using the intial condition of $X_1(t=0)$ we obtain the value of the integration constant. 

```math
\begin{align}
X_1(0) &=\frac{-1}{k_1 \cdot 0 + C} = \frac{-1}{C}  \tag{29}\\
C &= \frac{-1}{X_1(0)}  \tag{30}\\
\end{align}
```
##
so the final equation of is
```math
\begin{align}
X_1(t) &=\frac{-1}{k_1  t - \frac{1}{X_1(0)} } \tag{31}
\end{align}
```
##
so, multiplying numerator and denominator of the right side of the equation by $-X_1(0)$ we obtain
```math
\begin{align}
X_1(t) &=\frac{(-1)\cdot(-X_1(0))}{(-X_1(0))(k_1 \cdot t  - \frac{1}{X_1(0)}) }= \frac{X_1(0)}{(1-X_1(0)k_1\cdot t)}\tag{32}
\end{align}
```
"

